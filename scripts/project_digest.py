#!/usr/bin/env python3
"""
project_digest.py - per-project daily status digest (PIL Phase 1).

For each ACTIVE project, pulls the last N days of events from the Supabase
project_events store, asks Claude for a tight "what changed / what needs action"
summary, and overwrites reference/projects/<id>-<name>/status.md. The next
morning's /prime then surfaces project movement without anyone touching Outlook.

Reuses the router's registry loader, .env loader, and Claude model. Runs on the
VPS cron (always-on); no dependency on a desktop being open.

Run:
    .venv/bin/python scripts/project_digest.py            # last 1 day
    .venv/bin/python scripts/project_digest.py --days 2
    .venv/bin/python scripts/project_digest.py --telegram  # also push the morning brief

HARD RULE: no long dashes anywhere in this file or its output.
"""
import argparse
import datetime
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))

import email_router as er          # noqa: E402  (registry, .env, MODEL, telegram)
import supabase_events             # noqa: E402
import claude_max                  # noqa: E402  (Max-subscription Claude calls)

SYSTEM = (
    "You write tight daily project status updates for a structural-steel project "
    "manager. Given a list of events (emails and platform notifications) from the "
    "last few days on ONE project, produce at most 5 markdown bullet points "
    "covering what changed and what needs action. Collapse duplicate or trivial "
    "notifications into one bullet. Lead with anything that needs a reply or has a "
    "deadline. "
    "Treat the events as threads, not isolated items: if a later event shows an "
    "action has been replied to, answered, resolved, confirmed, closed or "
    "superseded, do NOT present the earlier item as still open or still needing a "
    "reply. Drop a fully-closed item entirely unless it carries useful residual "
    "information, in which case state it in the past tense as done. Only flag "
    "something as needing action if nothing in the events indicates it has already "
    "been handled. An event from the manager (source 'Manual') stating he has "
    "replied, confirmed or actioned something closes the matching open item. "
    "Plain Australian English, concrete, no preamble, no closing remark. "
    "Never use long dash characters; use a hyphen or colon. Output only the bullets."
)


def event_line(ev):
    parts = [ev.get("event_date", "?"), f"[{ev.get('event_type', 'General')}]",
             (ev.get("subject") or "").strip()]
    line = " ".join(p for p in parts if p)
    if ev.get("summary"):
        line += f" - {ev['summary'].strip()}"
    extra = []
    if ev.get("action_owner"):
        extra.append(f"owner: {ev['action_owner']}")
    if ev.get("due_date"):
        extra.append(f"due: {ev['due_date']}")
    if extra:
        line += f" ({'; '.join(extra)})"
    return line


def summarise(project_name, events):
    user = (f"Project: {project_name}\nEvents (most recent first):\n"
            + "\n".join(f"- {event_line(e)}" for e in events))
    # Subscription-billed via claude_max (forces Max auth, never the API key).
    return claude_max.complete(user, system=SYSTEM, model="sonnet").strip()


def send_morning_brief(blocks, day):
    """Push the substantive per-project brief to Telegram (the same bullets
    written to each status.md), chunked under the 4096-char message cap. Only
    projects that moved are included; a quiet morning sends a one-line note."""
    header = f"**Morning brief - {day}**"
    if not blocks:
        er.telegram_alert(f"{header}\n\nNothing new across the active projects.")
        return
    chunks, cur = [], header
    for blk in blocks:
        piece = "\n\n" + blk
        if len(cur) + len(piece) > 3800:
            chunks.append(cur)
            cur = blk
        else:
            cur += piece
    chunks.append(cur)
    for c in chunks:
        er.telegram_alert(c)


def write_status(folder, pid, name, days, body, n_events, stamp):
    md = (f"# {pid} {name} - Status\n\n"
          f"_Auto-generated by project_digest.py at {stamp}. "
          f"Window: last {days} day(s). Source: Supabase project_events._\n\n")
    if n_events == 0:
        md += f"- No new events in the last {days} day(s)."
    else:
        md += body + f"\n\n---\n_{n_events} event(s) in window._"
    (folder / "status.md").write_text(md + "\n", encoding="utf-8")


def main():
    ap = argparse.ArgumentParser(description="Per-project daily status digest")
    ap.add_argument("--days", type=int, default=1, help="lookback window in days")
    ap.add_argument("--telegram", action="store_true",
                    help="also push the per-project morning brief to Telegram")
    args = ap.parse_args()

    er.load_dotenv()
    import os
    if not os.environ.get("CLAUDE_CODE_OAUTH_TOKEN"):
        print("[digest] No CLAUDE_CODE_OAUTH_TOKEN in env; relying on an interactive "
              "Claude login. Set it in .env for the VPS cron (run `claude setup-token`).")

    stamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    briefs = sorted(er.PROJECTS_DIR.glob("*/project-brief.md"))
    brief_blocks = []
    written = 0
    for brief in briefs:
        header = er.parse_yaml_header(brief.read_text(encoding="utf-8"))
        pid = str(header.get("project_id", "")).strip()
        if not pid or str(header.get("status", "")).lower() != "active":
            continue
        name = header.get("project_name", "")
        try:
            events = supabase_events.recent_for_project(pid, days=args.days)
        except Exception as e:  # noqa: BLE001
            print(f"[{pid}] store query failed: {e}")
            continue
        body = summarise(name, events) if events else ""
        write_status(brief.parent, pid, name, args.days, body, len(events), stamp)
        written += 1
        print(f"[{pid}] {name}: {len(events)} event(s) -> status.md")
        if events and body.strip():
            brief_blocks.append(f"**{pid} {name}**\n{body.strip()}")

    print(f"\nWrote {written} status.md files.")
    if args.telegram:
        send_morning_brief(brief_blocks, datetime.date.today().isoformat())


if __name__ == "__main__":
    main()
