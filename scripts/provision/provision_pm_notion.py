#!/usr/bin/env python3
"""
provision_pm_notion.py - create a PM's own Notion database set.

Mirrors Nathan's schemas (read live from his registry DBs as the template) and
creates, under the PM's own parent page / teamspace:
  - one General Notes DB
  - one Subcontractors Diary DB per project
  - one Voice Memo Log DB per project
then writes the PM's site-diary registry (the per-instance Notion config the
capture workers read). The shared Dunsteel integration is what creates them, so
it can already write to all of them; isolation comes from the parent page being
the PM's private teamspace (see the runbook).

DRY-RUN by default: reads the template schemas and prints the plan. Pass --apply
to actually create the databases.

    python scripts/provision/provision_pm_notion.py --pm alex --parent <page_id>
    python scripts/provision/provision_pm_notion.py --pm alex --parent <page_id> --apply

HARD RULE: no long dashes anywhere in this file.
"""

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
import common  # noqa: E402

NATHAN_REGISTRY = common.WORKSPACE / "outputs" / "internal" / "site-diary-db-registry.json"


def _template_ids(reg: dict):
    """Pick Nathan's DBs to use as schema templates: general_notes, and the
    first project's diary + voice memo log."""
    gn = reg.get("general_notes")
    proj_keys = [k for k in reg if k not in ("_note", "general_notes", "voice_memo_logs")
                 and isinstance(reg[k], str)]
    diary_tpl = reg[proj_keys[0]] if proj_keys else None
    vml_map = reg.get("voice_memo_logs", {})
    vml_tpl = vml_map.get(proj_keys[0]) if proj_keys else None
    return gn, diary_tpl, vml_tpl


def _create_db(key, parent, title, props, apply):
    if not apply:
        print(f"    would create: {title}  ({len(props)} properties)")
        return f"<dry-run:{title}>"
    payload = {"parent": {"type": "page_id", "page_id": parent},
               "title": [{"text": {"content": title}}],
               "properties": props}
    db = common.notion_post("databases", key, payload)
    db_id = db.get("id")
    print(f"    created: {title}  -> {db_id}")
    return db_id


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--pm", required=True, help="canonical PM key, e.g. alex")
    ap.add_argument("--parent", help="Notion parent page/teamspace id (required for --apply)")
    ap.add_argument("--projects", help="comma-separated job numbers; else derived from Airtable")
    ap.add_argument("--out", help="where to write the PM registry json")
    ap.add_argument("--apply", action="store_true", help="actually create DBs (default: dry-run)")
    args = ap.parse_args()
    apply = args.apply

    env = common.load_env()
    key = env.get("NOTION_API_KEY")
    if not key:
        sys.exit("Missing NOTION_API_KEY in .env")
    if apply and not args.parent:
        sys.exit("--parent <page_id> is required with --apply")

    reg = json.loads(NATHAN_REGISTRY.read_text(encoding="utf-8"))
    gn_tpl, diary_tpl, vml_tpl = _template_ids(reg)
    if not (gn_tpl and diary_tpl and vml_tpl):
        sys.exit("Could not resolve template DBs from Nathan's registry")

    # Projects for this PM.
    if args.projects:
        projects = [p.strip() for p in args.projects.split(",") if p.strip()]
    else:
        projects = [p["number"] for p in common.pm_projects(args.pm, env)]
    if not projects:
        sys.exit(f"No projects found for PM '{args.pm}'. Pass --projects to override.")

    print(f"PM: {args.pm}   projects: {', '.join(projects)}")
    print(f"Mode: {'APPLY (creating live)' if apply else 'DRY-RUN (no writes)'}\n")

    # Read template schemas once.
    gn_props = common.clonable_properties(common.notion_db_schema(gn_tpl, key))
    diary_props = common.clonable_properties(common.notion_db_schema(diary_tpl, key))
    vml_props = common.clonable_properties(common.notion_db_schema(vml_tpl, key))

    pm_title = args.pm.capitalize()
    new_reg = {"_note": f"Per-PM site-diary registry for {args.pm}. Generated by "
                        "provision_pm_notion.py. Run scripts/agent/verify_registry.py "
                        "from the PM workspace after deploy."}

    print("General Notes:")
    new_reg["general_notes"] = _create_db(
        key, args.parent, f"{pm_title} - General Notes", gn_props, apply)

    new_reg["voice_memo_logs"] = {}
    for proj in projects:
        print(f"Project {proj}:")
        new_reg[proj] = _create_db(
            key, args.parent, f"{pm_title} - Subcontractors Diary {proj}", diary_props, apply)
        new_reg["voice_memo_logs"][proj] = _create_db(
            key, args.parent, f"{pm_title} - Voice Memo Log {proj}", vml_props, apply)

    out = Path(args.out) if args.out else (
        Path(__file__).resolve().parent / f"{args.pm}-site-diary-db-registry.local.json")
    out.write_text(json.dumps(new_reg, indent=2) + "\n", encoding="utf-8")
    print(f"\nWrote PM registry: {out}")
    if not apply:
        print("DRY-RUN: ids are placeholders. Re-run with --apply to create the DBs.")
    else:
        print("Copy this file to the PM workspace at "
              "outputs/internal/site-diary-db-registry.json")


if __name__ == "__main__":
    main()
