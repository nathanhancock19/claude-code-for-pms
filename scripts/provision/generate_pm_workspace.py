#!/usr/bin/env python3
"""
generate_pm_workspace.py - fill a PM's workspace clone with their own context.

Given a fresh clone of the template repo, this writes the per-PM context files
(who they are, their projects) from the PM directory + the Day Docket Airtable,
and reports which reference/projects/<id> folders to keep (their jobs) vs remove.

DRY-RUN by default: prints what it would write/remove. Pass --apply to write into
--dest (the PM's clone). Never touches Nathan's workspace unless --dest points at
it (it will refuse if --dest is the current repo).

    python scripts/provision/generate_pm_workspace.py --pm alex --dest /path/to/alex-clone
    python scripts/provision/generate_pm_workspace.py --pm alex --dest /path/to/alex-clone --apply

HARD RULE: no long dashes anywhere in this file.
"""

import argparse
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
import common  # noqa: E402
import dunsteel_projects as dp  # noqa: E402


def _pm_context(pm_label, projects):
    lines = "\n".join(f"- **{p['number']}** {p['name']}" for p in projects) or "- (none yet)"
    return f"""# PM Context - {pm_label}

> Per-PM context for {pm_label}'s Dunsteel workspace. Generated by
> generate_pm_workspace.py from the Day Docket Airtable. Edit freely after setup.

## Who owns this workspace

{pm_label}: Project Manager at Dunsteel (structural steel fabrication and install, Sydney).
This is {pm_label}'s own instance of the Dunsteel PM workspace. The Telegram brain,
voice capture, and daily brief all run scoped to {pm_label}'s projects below.

## {pm_label}'s active projects

{lines}

## Notes

- Voice capture and the Subcontractors Diary are scoped to the project numbers above.
- Add project briefs under reference/projects/<id>/ as jobs ramp up.
"""


def _dunsteel_projects(pm_label, projects):
    rows = "\n".join(f"| {p['number']} | {p['name']} | Active |" for p in projects) \
        or "| - | (none yet) | - |"
    return f"""# Dunsteel Projects - {pm_label}

> {pm_label}'s active project list, derived from the Day Docket Airtable.

| Number | Name | Status |
|---|---|---|
{rows}
"""


def _current_data(pm_label, projects):
    nums = ", ".join(p["number"] for p in projects) or "(none yet)"
    return f"""# Current Data - {pm_label}

> Live status snapshot for {pm_label}'s projects. Updated by the digest / daily brief.

## Active projects

{nums}

(Status entries populate as the email router and digest run for this instance.)
"""


def _personal_info(pm_label, email):
    return f"""# Personal Info

- **Name:** {pm_label}
- **Role:** Project Manager, Dunsteel (structural steel fabrication and install)
- **Email:** {email}
- **Workspace:** {pm_label}'s own instance of the Dunsteel PM workspace.
"""


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--pm", required=True, help="canonical PM key, e.g. alex")
    ap.add_argument("--dest", required=True, help="path to the PM's workspace clone")
    ap.add_argument("--projects", help="comma-separated job numbers; else derived from Airtable")
    ap.add_argument("--apply", action="store_true", help="write files (default: dry-run)")
    args = ap.parse_args()

    pm_key = dp.pm_key(args.pm)
    if not pm_key:
        sys.exit(f"Unknown PM '{args.pm}'. Known: {', '.join(dp.pm_keys())}")
    dest = Path(args.dest).resolve()
    if dest == common.WORKSPACE:
        sys.exit("Refusing to run against the current repo. --dest must be the PM's own clone.")
    if args.apply and not dest.exists():
        sys.exit(f"--dest does not exist: {dest}. Clone the template there first.")

    env = common.load_env()
    if args.projects:
        projects = [{"number": n.strip(), "name": ""} for n in args.projects.split(",") if n.strip()]
    else:
        projects = common.pm_projects(pm_key, env)

    pm_label = dp.pm_label(pm_key)
    email = dp.pm_email(pm_key)
    print(f"PM: {pm_key} ({pm_label})   projects: {', '.join(p['number'] for p in projects) or '(none)'}")
    print(f"Dest: {dest}")
    print(f"Mode: {'APPLY' if args.apply else 'DRY-RUN'}\n")

    files = {
        "context/pm-context.md": _pm_context(pm_label, projects),
        "context/dunsteel-projects.md": _dunsteel_projects(pm_label, projects),
        "context/current-data.md": _current_data(pm_label, projects),
        "context/personal-info.md": _personal_info(pm_label, email),
    }
    for rel, content in files.items():
        target = dest / rel
        if args.apply:
            target.parent.mkdir(parents=True, exist_ok=True)
            target.write_text(content, encoding="utf-8")
            print(f"  wrote {rel}")
        else:
            print(f"  would write {rel}  ({len(content)} chars)")

    # reference/projects trim: keep only this PM's job folders.
    keep = {p["number"] for p in projects}
    proj_root = dest / "reference" / "projects"
    print("\nreference/projects/ trim:")
    if proj_root.exists():
        for d in sorted(proj_root.iterdir()):
            if not d.is_dir():
                continue
            num = d.name.split("-")[0]
            if num in keep:
                print(f"  keep   {d.name}")
            elif args.apply:
                import shutil
                shutil.rmtree(d)
                print(f"  removed {d.name}")
            else:
                print(f"  would remove {d.name}")
    else:
        print("  (no reference/projects in dest yet)")

    print("\nNext: build_pm_env.py, then provision_pm_notion.py, then deploy_pm_bot.py.")


if __name__ == "__main__":
    main()
